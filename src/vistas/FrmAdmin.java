    private void btnKiscosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKiscosActionPerformed
        // TODO add your handling code here:

        if (!txtCodeKioscos.getText().isEmpty()
                && !txtNombreKiosco.getText().isEmpty()) {

            Kiosco kiosco = new Kiosco(txtCodeKioscos.getText(), txtNombreKiosco.getText(), jCKiscoRegion.getSelectedItem().toString());
            //limpiar
            txtCodeKioscos.setText("");
            txtNombreKiosco.setText("");

            if (Principal.agregarKisco(kiosco)) {
                JOptionPane.showMessageDialog(this, "Se agregro kisco, exitosamente");
                System.out.println(kiosco);
            } else {
                JOptionPane.showMessageDialog(this, "Codigo de kiosco repetido");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Debe de agregar los datos necesarios");
        }

    }//GEN-LAST:event_btnKiscosActionPerformed


