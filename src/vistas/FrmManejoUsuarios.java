    public void listarTarjetas() {
        DefaultTableModel defaultTableModel = new DefaultTableModel(new String[]{"Nombre Tarjeta", "Tarjeta", "fecha"}, RegistroTarjetaDao.registroTarjeta.size());
        jTable2.setModel(defaultTableModel);

        TableModel modeloDatos = jTable2.getModel();

        for (Integer i = 0; i < RegistroTarjetaDao.getSalesById(id).size(); i++) {
            RegistroTarjeta registroTarjeta = RegistroTarjetaDao.getSalesById(id).get(i);
            modeloDatos.setValueAt(registroTarjeta.getNombreTarjeta(), i, 0);
            modeloDatos.setValueAt(registroTarjeta.getNumTarjeta(), i, 1);
            modeloDatos.setValueAt(registroTarjeta.getFechaVencimiento(), i, 2);

        }
    }

