    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:

        int row = jTable2.getSelectedRow();

        jTextField3.setText(jTable2.getModel().getValueAt(row, 0).toString());
        jTextField5.setText(jTable2.getModel().getValueAt(row, 1).toString());
        jTextField4.setText(jTable2.getModel().getValueAt(row, 2).toString());

    }//GEN-LAST:event_jTable2MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int i = jTable2.getSelectedRow();

        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();

        int modificar = jTable2.getSelectedRow();
        if (!jTextField3.getText().isEmpty() && !jTextField5.getText().isEmpty() && !jTextField4.getText().isEmpty()) {
            if (modificar >= 0) {
                Principal.editarTarjetas(jTextField3.getText(), jTextField5.getText(), jTextField4.getText());
                JOptionPane.showMessageDialog(this, "Tarjeta Actualizado");

          
                model.setValueAt(jTextField5.getText(), i, 1);
                model.setValueAt(jTextField4.getText(), i, 2);

                jTextField3.setText("");
                jTextField5.setText("");
                jTextField4.setText("");

            } else {
                JOptionPane.showMessageDialog(this, "Debes de seleccionar un tarjeta", "Actualizar", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debes de llenar los campos necesario", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButton2ActionPerformed

